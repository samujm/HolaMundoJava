
public class CaracteresEspeciales {

    public static void main(String args[]) {
        var nombre = "Karla";

        System.out.println("Nueva linea: \n" + nombre); //Salto de line con \n
        System.out.println("Tabulador: \t" + nombre); //Tabulador \t
        System.out.println("Retroceso: \b\b" + nombre); //Regresa una posici√≥n en la cantidad de caracteres \b
        System.out.println("Comilla simple: \'" + nombre + "\'"); //Visualizar comilla simple
        System.out.println("Comilla doble: \"" + nombre + "\" "); //Visualizar comilla doble
      
    }
}
